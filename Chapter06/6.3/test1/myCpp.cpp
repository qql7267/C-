#include <iostream>

int main()
{
	/*
	字符函数库cctype
	这是一个与字符相关的 非常方便的函数软件包 可以简化诸如确定字符是否为大写字母、数字、标点符号等工作
	*/

	/*
	cctype中的字符函数
	函数名称		返回值
	isalnum()	如果参数是字母或数字 返回true
	isalpha()	如果参数是字母 返回true
	iscntrl()	如果参数是控制字符 返回true
	isdigit()	如果参数是数字(0~9) 返回true
	isgraph()	如果参数是除了空格以外的打印字符 返回true
	islower()	如果参数是小写字母 返回true
	isprint()	如果参数是打印字符(包括空格) 返回true
	ispunct()	如果参数是标点字符 返回true
	isspace()	如果参数是标准白字符 如空格、进制、换行符、回车、水平制表符或者垂直制表符 返回true
	isupper()	如果参数是大写字母 返回true
	isxdigit()	如果参数是十六进制数字 如0~9、a~f或A~F 返回true
	tolower()	如果参数是大写字母 返回其小写 否则返回该参数
	toupper()	如果参数是小写字母 返回其大写 否则返回该参数
	*/

	return 0;
}